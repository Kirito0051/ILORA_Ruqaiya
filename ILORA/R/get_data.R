#' Retrieve Data for a Given Species from the ILORA Database
#'
#' This function searches for a specified species within all tables of the ILORA database schema and retrieves the desired variables.
#' The search is performed across all relevant tables to ensure comprehensive data retrieval for the specified species.
#'
#' @param species_name A character string representing the species name to search for in the database tables.
#' @param variables A character vector specifying the column names to retrieve from the tables.
#' @return A data frame containing the retrieved data, or NULL if no data is found.
#' @details
#'
#' ### To fetch Species names available in Database
#' All species names available in database can be retrieved using the function:
#' \code{get_species_names()}
#'
#' ### To fetch Variable names available in database
#' All variable names available in database can be retrieved using the function:
#' \code{get_variable_names()}
#'
#' The following are examples of species names and variable names that can be used:
#' - Species names: 'Rotala densiflora (Roth) Koehne','Rotala indica (Willd.) Koehne','Rubus buergeri Miq.',
#' - Variable names: "orders", "0700_Fuels2", "genus", "species"
#' @export



get_data <- function(species_name, variables) {
  con <- dbConnect(
    RPostgres::Postgres(),
    dbname = "postgres",
    host = "localhost",
    port = 5432,
    user = "postgres",
    password = "azaz1010"
  )

  if (is.null(con)) {
    stop("Failed to connect to the database.")
  }

  data <- NULL

  # Check if any species name is incorrect or not present
  species_names_list <- paste(sprintf("'%s'", species_name), collapse = ", ")
  query_check_species <- sprintf('SELECT DISTINCT acc_species_name FROM joint_data WHERE acc_species_name IN (%s)', species_names_list)
  temp_species_check <- dbGetQuery(con, query_check_species)

  if (is.null(temp_species_check) || nrow(temp_species_check) != length(species_name)) {
    message("Please recheck and enter correct species name(s).")
    dbDisconnect(con)
    return(NULL)
  }

  # Check if any variable name is incorrect or not present
  query_check_variables <- sprintf('SELECT column_name FROM information_schema.columns WHERE table_name = \'joint_data\'')
  temp_variables_check <- dbGetQuery(con, query_check_variables)

  valid_variables <- c("acc_species_name", variables)
  if (!all(valid_variables %in% temp_variables_check$column_name)) {
    message("Please recheck and enter correct variable name(s).")
    dbDisconnect(con)
    return(NULL)
  }

  # Constructing SELECT query with DISTINCT and double quotes around variables
  select_clause <- paste(sprintf('"%s"', variables), collapse = ", ")
  select_clause <- paste0('"acc_species_name", ', select_clause)
  query <- sprintf('SELECT DISTINCT %s FROM joint_data WHERE acc_species_name IN (%s)', select_clause, species_names_list)

  temp_data <- dbGetQuery(con, query)

  dbDisconnect(con)  # Disconnect after query

  if (is.null(temp_data) || nrow(temp_data) == 0) {
    message("No data found for the specified species names and variables.")
    return(NULL)
  } else {
    data <- rbind(data, temp_data)
    return(data)
  }



}
